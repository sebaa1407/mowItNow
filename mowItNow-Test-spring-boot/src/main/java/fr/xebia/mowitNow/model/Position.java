public class Position {
	
	/*
	 * abscissa of point.
	 */
	private int x;
	/*
	 * ordinate of point.
	 */
	private int y;


	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

}
